package LessonActivities.Loop;

 class Main {
    public static void main() {
        int num = 1;
        // While loop
        while (num <= 10) {
            System.out.println("Hello There");
            num++;
        }

        // do while loop
        do {
            System.out.println("Hello There");
            num++;
        } while (num <= 10);

    }
}